package uz.exemple.less52_imoui_contacts_java.model;

public class ContactTopModel {
    int icon;
    String title;

    public ContactTopModel(int icon, String title) {
        this.icon = icon;
        this.title = title;
    }

    public int getIcon() {
        return icon;
    }

    public String getTitle() {
        return title;
    }
}
